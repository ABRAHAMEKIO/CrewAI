function singleNested() {
  const arr = [];

  function printValues(obj) {
    // eslint-disable-next-line no-restricted-syntax
    for (const key in obj) {
      if ('name' in obj[key]) {
        if ('options' in obj[key]) {
          printValues(obj[key].options);
        }
        arr.push({
          ...obj[key],
          prev: obj.map((d) => d.name),
          next:
            'options' in obj[key] ? obj[key].options.map((d) => d.name) : [],
        });
      }
    }
  }

  printValues(Prompt);
  return arr;
}

